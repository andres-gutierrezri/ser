#!/bin/bash

echo "================================================================"
echo "INICIADOR DE APLICACI√ìN DJANGO CON ENTORNO VIRTUAL"
echo "================================================================"

# Cambiar al directorio del script
cd "$(dirname "$0")"

# Verificar que estamos en el directorio correcto
echo "üîç Verificando estructura del proyecto..."
if [ ! -f "manage.py" ]; then
    echo "‚ùå Error: No se encontr√≥ manage.py en el directorio actual"
    echo "   Aseg√∫rate de que el script est√© en la carpeta ra√≠z del proyecto Django"
    exit 1
fi
echo "‚úÖ Estructura del proyecto verificada"

echo "üîç Detectando y configurando bibliotecas de MySQL..."
# Buscar instalaci√≥n de MySQL en ubicaciones comunes de macOS
MYSQL_BASE_PATHS=(
    "/usr/local/mysql"
    "/opt/homebrew/opt/mysql"
    "/usr/local/opt/mysql"
    "/opt/homebrew/Cellar/mysql/"*
    "/usr/local/Cellar/mysql/"*
)

MYSQL_ROOT=""
for path_pattern in "${MYSQL_BASE_PATHS[@]}"; do
    # Expandir patrones con comodines
    for path in $path_pattern; do
        if [ -d "$path/lib" ] && [ -f "$path/lib/libmysqlclient.dylib" -o -f "$path/lib/libmysqlclient.24.dylib" ]; then
            MYSQL_ROOT="$path"
            break 2
        fi
    done
done

if [ -n "$MYSQL_ROOT" ]; then
    # Configurar todas las variables de entorno necesarias para MySQL
    export PATH="$MYSQL_ROOT/bin:$PATH"
    export MYSQLCLIENT_CFLAGS="-I$MYSQL_ROOT/include"
    export MYSQLCLIENT_LDFLAGS="-L$MYSQL_ROOT/lib -lmysqlclient"
    export PKG_CONFIG_PATH="$MYSQL_ROOT/lib/pkgconfig"
    export DYLD_LIBRARY_PATH="$MYSQL_ROOT/lib:$DYLD_LIBRARY_PATH"
    export DYLD_FALLBACK_LIBRARY_PATH="$MYSQL_ROOT/lib:$DYLD_FALLBACK_LIBRARY_PATH"
    
    echo "‚úÖ MySQL encontrado en: $MYSQL_ROOT"
    echo "   Variables de entorno configuradas:"
    echo "   - PATH"
    echo "   - MYSQLCLIENT_CFLAGS"
    echo "   - MYSQLCLIENT_LDFLAGS"
    echo "   - PKG_CONFIG_PATH"
    echo "   - DYLD_LIBRARY_PATH"
    echo "   - DYLD_FALLBACK_LIBRARY_PATH"
else
    echo "‚ö†Ô∏è  No se encontraron bibliotecas de MySQL en ubicaciones est√°ndar"
    echo "   Si usas MySQL, aseg√∫rate de tener MySQL instalado correctamente"
    echo "   Puedes instalar MySQL con: brew install mysql"
    echo ""
    echo "   O agregar estas variables a tu ~/.zprofile:"
    echo "   export PATH=\"/usr/local/mysql/bin:\$PATH\""
    echo "   export MYSQLCLIENT_CFLAGS=\"-I/usr/local/mysql/include\""
    echo "   export MYSQLCLIENT_LDFLAGS=\"-L/usr/local/mysql/lib -lmysqlclient\""
    echo "   export PKG_CONFIG_PATH=\"/usr/local/mysql/lib/pkgconfig\""
    echo "   export DYLD_LIBRARY_PATH=\"/usr/local/mysql/lib:\$DYLD_LIBRARY_PATH\""
    echo "   export DYLD_FALLBACK_LIBRARY_PATH=\"/usr/local/mysql/lib:\$DYLD_FALLBACK_LIBRARY_PATH\""
fi

echo "üîç Verificando entorno virtual..."
if [ ! -d ".venv" ]; then
    echo "üî® Creando entorno virtual..."
    python3 -m venv .venv
    if [ $? -ne 0 ]; then
        echo "‚ùå Error al crear entorno virtual"
        exit 1
    fi
    echo "‚úÖ Entorno virtual creado exitosamente"
else
    echo "‚úÖ Entorno virtual ya existe"
fi

echo "üîé Verificando estado del entorno virtual..."
if [ -n "$VIRTUAL_ENV" ]; then
    echo "‚úÖ Entorno virtual ya est√° activado: $VIRTUAL_ENV"
else
    echo "‚ö†Ô∏è  Entorno virtual no est√° activado, se proceder√° a activarlo:"
    echo "üêç Activando entorno virtual..."
    VENV_ACTIVATE_PATH="$(pwd)/.venv/bin/activate"
    echo "   Ruta de activaci√≥n: $VENV_ACTIVATE_PATH"
    source .venv/bin/activate
    if [ $? -ne 0 ]; then
        echo "‚ùå Error al activar entorno virtual"
        exit 1
    fi
    echo "‚úÖ Entorno virtual activado exitosamente"
fi

echo "üìç Informaci√≥n del entorno virtual:"
echo "   Ruta: $(pwd)/.venv"
echo "   Activaci√≥n manual: source .venv/bin/activate"
echo "   Python: .venv/bin/python"

echo "üîß Verificando integridad de pip..."
.venv/bin/python -c "import pip" 2>/dev/null
if [ $? -ne 0 ]; then
    echo "‚ö†Ô∏è  Pip est√° corrupto, reinstalando entorno virtual..."
    echo "üóëÔ∏è  Eliminando entorno virtual existente..."
    rm -rf .venv
    echo "üî® Creando nuevo entorno virtual..."
    python3 -m venv .venv
    if [ $? -ne 0 ]; then
        echo "‚ùå Error al crear nuevo entorno virtual"
        exit 1
    fi
    echo "üêç Activando nuevo entorno virtual..."
    source .venv/bin/activate
    if [ $? -ne 0 ]; then
        echo "‚ùå Error al activar nuevo entorno virtual"
        exit 1
    fi
    echo "‚úÖ Nuevo entorno virtual creado y activado"
    echo "üìç Ruta de activaci√≥n: $(pwd)/.venv/bin/activate"
else
    echo "‚úÖ Pip funciona correctamente"
    echo "‚¨ÜÔ∏è  Actualizando pip..."
    .venv/bin/python -m pip install --upgrade pip 2>/dev/null
    if [ $? -ne 0 ]; then
        echo "üîß Intentando reparar pip con ensurepip..."
        .venv/bin/python -m ensurepip --upgrade 2>/dev/null
        if [ $? -ne 0 ]; then
            echo "‚ö†Ô∏è  Ensurepip fall√≥, intentando actualizaci√≥n forzada..."
            .venv/bin/python -m pip install --upgrade --force-reinstall pip 2>/dev/null
            if [ $? -ne 0 ]; then
                echo "‚ö†Ô∏è  No se pudo actualizar pip, pero continuando con la versi√≥n actual..."
            else
                echo "‚úÖ Pip actualizado con reinstalaci√≥n forzada"
            fi
        else
            echo "‚úÖ Pip reparado con ensurepip"
            echo "‚¨ÜÔ∏è  Actualizando pip..."
            .venv/bin/python -m pip install --upgrade pip 2>/dev/null
        fi
    else
        echo "‚úÖ Pip actualizado exitosamente"
    fi
fi

echo "üßπ Limpiando cach√© de pip..."
pip3 cache purge 2>/dev/null
if [ $? -eq 0 ]; then
    echo "‚úÖ Cach√© de pip limpiado exitosamente"
else
    echo "‚ö†Ô∏è  No se pudo limpiar el cach√© de pip (puede ser normal)"
fi

echo "üì¶ Instalando dependencias..."
if [ -f "requirements.txt" ]; then
    echo "üîç Verificando pip antes de instalar dependencias..."
    .venv/bin/python -m pip --version 2>/dev/null
    if [ $? -ne 0 ]; then
        echo "‚ö†Ô∏è  Pip no responde, reinstalando pip..."
        .venv/bin/python -m ensurepip --upgrade --force 2>/dev/null
        if [ $? -ne 0 ]; then
            echo "‚ùå Error al reinstalar pip"
            exit 1
        fi
    fi

    echo "üìã Instalando desde requirements.txt (sin cach√©)..."
    .venv/bin/python -m pip install -r requirements.txt --no-cache-dir
    if [ $? -ne 0 ]; then
        echo "‚ö†Ô∏è  Error en instalaci√≥n, intentando con reinstalaci√≥n forzada..."
        .venv/bin/python -m pip install -r requirements.txt --no-cache-dir --force-reinstall
        if [ $? -ne 0 ]; then
            echo "‚ùå Error al instalar dependencias"
            echo ""
            echo "üí° Sugerencias para solucionar el problema:"
            echo "   1. Verificar conexi√≥n a internet"
            echo "   2. Ejecutar desde terminal: source .venv/bin/activate && pip install --upgrade pip"
            echo "   3. Instalar dependencias manualmente una por una"
            echo ""
            read -p "üîÑ ¬øDesea intentar recrear el entorno virtual? (s/n): " recreate
            if [ "$recreate" = "s" ] || [ "$recreate" = "S" ]; then
                echo "üîÑ Recreando entorno virtual..."
                rm -rf .venv 2>/dev/null
                python3 -m venv .venv
                if [ $? -ne 0 ]; then
                    echo "‚ùå Error al recrear entorno virtual"
                    exit 1
                fi
                source .venv/bin/activate
                .venv/bin/python -m pip install --upgrade pip
                .venv/bin/python -m pip install -r requirements.txt --no-cache-dir
                if [ $? -ne 0 ]; then
                    echo "‚ùå Error persistente al instalar dependencias"
                    exit 1
                else
                    echo "‚úÖ Dependencias instaladas despu√©s de recrear entorno virtual"
                fi
            else
                echo "‚ùå No se pudieron instalar las dependencias"
                exit 1
            fi
        else
            echo "‚úÖ Dependencias instaladas exitosamente (sin cach√©)"
        fi
    else
        echo "‚úÖ Dependencias instaladas exitosamente"
    fi
else
    echo "‚ö†Ô∏è  No se encontr√≥ requirements.txt, continuando sin instalar dependencias..."
fi

echo ""
echo "üì¶ Paquetes instalados en el entorno virtual:"
echo "================================================================"
.venv/bin/python -m pip list --format=columns 2>/dev/null
if [ $? -ne 0 ]; then
    echo "‚ö†Ô∏è  No se pudo obtener la lista de paquetes"
else
    echo "================================================================"
fi
echo ""

echo "üìÅ Recolectando archivos est√°ticos..."
.venv/bin/python manage.py collectstatic --noinput
if [ $? -ne 0 ]; then
    echo "‚ö†Ô∏è  Advertencia al recolectar archivos est√°ticos (puede ser normal si no est√° configurado)"
fi

echo "üîÑ Ejecutando construcci√≥n de migraciones..."
.venv/bin/python manage.py makemigrations
if [ $? -ne 0 ]; then
    echo "‚ö†Ô∏è  Advertencia al ejecutar makemigrations (puede ser normal si no hay cambios)"
fi

echo "üîÑ Ejecutando migraciones..."
.venv/bin/python manage.py migrate

if [ $? -ne 0 ]; then
    echo "‚ùå Error al ejecutar migraciones"
    exit 1
fi

echo "‚úÖ Migraciones completadas"

echo "üë§ Creando superusuario por defecto (si no existe)..."
.venv/bin/python create_default_superuser.py
if [ $? -ne 0 ]; then
    echo "‚ö†Ô∏è  Advertencia al crear superusuario (puede ser normal si ya existe)"
fi

echo "üîç Verificando importaci√≥n de MySQLdb..."
.venv/bin/python -c "import MySQLdb; print('MySQLdb import successful')" 2>/dev/null
if [ $? -eq 0 ]; then
    echo "‚úÖ MySQLdb importado exitosamente"
else
    echo "‚ö†Ô∏è  Advertencia: MySQLdb no pudo ser importado"
    echo ""
    echo "üí° Soluciones posibles:"
    echo "   1. Instalar MySQL:"
    echo "      brew install mysql"
    echo ""
    echo "   2. Agregar variables de entorno de MySQL a ~/.zprofile:"
    echo "      export PATH=\"/usr/local/mysql/bin:\$PATH\""
    echo "      export MYSQLCLIENT_CFLAGS=\"-I/usr/local/mysql/include\""
    echo "      export MYSQLCLIENT_LDFLAGS=\"-L/usr/local/mysql/lib -lmysqlclient\""
    echo "      export PKG_CONFIG_PATH=\"/usr/local/mysql/lib/pkgconfig\""
    echo "      export DYLD_LIBRARY_PATH=\"/usr/local/mysql/lib:\$DYLD_LIBRARY_PATH\""
    echo "      export DYLD_FALLBACK_LIBRARY_PATH=\"/usr/local/mysql/lib:\$DYLD_FALLBACK_LIBRARY_PATH\""
    echo ""
    echo "   3. Reinstalar mysqlclient con las variables configuradas:"
    echo "      source .venv/bin/activate"
    echo "      pip uninstall mysqlclient -y"
    echo "      pip install mysqlclient --no-cache-dir"
    echo ""
    echo "   4. Si no usas MySQL, puedes cambiar a SQLite en settings.py"
    echo ""
    
    # Intentar detectar si MySQL est√° instalado
    if command -v mysql >/dev/null 2>&1; then
        MYSQL_CONFIG=$(which mysql_config 2>/dev/null)
        if [ -n "$MYSQL_CONFIG" ]; then
            MYSQL_LIB=$($MYSQL_CONFIG --libs 2>/dev/null | grep -o '\-L[^ ]*' | sed 's/-L//')
            if [ -n "$MYSQL_LIB" ]; then
                MYSQL_BASE=$(dirname "$MYSQL_LIB")
                echo "   üìå MySQL detectado en: $MYSQL_BASE"
                echo "   Aseg√∫rate de tener estas variables en tu ~/.zprofile"
            fi
        fi
    else
        echo "   ‚ö†Ô∏è  MySQL no parece estar instalado en el sistema"
    fi
    echo ""
fi

echo "üöÄ Iniciando servidor de desarrollo..."
echo "üìç URL de la aplicaci√≥n: http://127.0.0.1:8000/"
echo "‚èπÔ∏è  Presiona Ctrl+C para detener el servidor"

echo "üåê Abriendo navegador en segundo plano..."
sleep 3
open http://127.0.0.1:8000/

.venv/bin/python manage.py runserver 127.0.0.1:8000
if [ $? -ne 0 ]; then
    echo "‚ùå Error al iniciar el servidor de desarrollo"
    exit 1
fi
echo "‚úÖ Servidor de desarrollo detenido"
echo "================================================================"
echo "üëã Gracias por usar el iniciador de aplicaci√≥n Django"
echo "================================================================"
